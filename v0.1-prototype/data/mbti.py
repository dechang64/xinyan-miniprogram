def score_mbti(answers: list) -> dict:
    """计算 MBTI 4 维度得分

    Args:
        answers: list of 8 个字母 (E/I/S/N/T/F/J/P)
    Returns:
        {
            "type": "INFJ",
            "type_name": "提倡者",
            "desc": "内心有理想, 安静却有力量",
            "dimensions": {
                "EI": {"E": 1, "I": 1, "ratio_E": 0.5},
                "SN": {"S": 1, "N": 1, "ratio_S": 0.5},
                ...
            }
        }
    """
    if len(answers) != 8:
        return {"error": f"expected 8 answers, got {len(answers)}"}

    # 计数
    counts = {"E": 0, "I": 0, "S": 0, "N": 0, "T": 0, "F": 0, "J": 0, "P": 0}
    for a in answers:
        if a in counts:
            counts[a] += 1

    # 计算 4 维度
    dims = {}
    for left, right in [("E", "I"), ("S", "N"), ("T", "F"), ("J", "P")]:
        l, r = counts[left], counts[right]
        total = l + r
        dims[left + right] = {
            left: l,
            right: r,
            f"ratio_{left}": l / total if total else 0,
            f"ratio_{right}": r / total if total else 0,
        }

    # 选主导字母 (counts 多的)
    chosen = []
    for left, right in [("E", "I"), ("S", "N"), ("T", "F"), ("J", "P")]:
        chosen.append(left if counts[left] >= counts[right] else right)

    mbti_type = "".join(chosen)
    type_name, type_desc = MBTI_16_TYPES.get(mbti_type, ("未知", "需要再测一次"))

    return {
        "type": mbti_type,
        "type_name": type_name,
        "type_desc": type_desc,
        "dimensions": dims,
    }


